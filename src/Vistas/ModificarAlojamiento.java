    private void jBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBuscarActionPerformed
        // TODO add your handling code here:
        if(jCbModificar1.getItemCount() > 0){
        
            jCbModificar1.removeAllItems();
            
        }
        Ciudad bu = (Ciudad)jCbciudad.getSelectedItem();
        AlojamientoData al= new AlojamientoData();
        List<Alojamiento> alojamientos = al.listarAlojamientos();
        for(Alojamiento alojamiento : alojamientos){
            
            if(alojamiento.getCiudadDestino().getIdCiudad() == bu.getIdCiudad()){
            
                jCbModificar1.addItem(alojamiento);
                
            }
        
        }
        
        
        
    }//GEN-LAST:event_jBuscarActionPerformed


