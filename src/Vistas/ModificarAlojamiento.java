    private void cargarCombo() {
        
        AlojamientoData cd = new AlojamientoData();
        List<Alojamiento> alojamientos = cd.listarAlojamientos();
        for (Alojamiento alojamiento : alojamientos) {
            jCbModificar1.addItem(alojamiento);
            //cbCiudadDestino.addItem(ciudad);
        }
    }
    
    private void cargarCombo2() {
        CiudadData cd = new CiudadData();
        List<Ciudad> ciudades = cd.listarCiudades();
        for (Ciudad ciudad : ciudades) {
            jCbciudad.addItem(ciudad);
            //cbCiudadDestino.addItem(ciudad);
        }
    }

