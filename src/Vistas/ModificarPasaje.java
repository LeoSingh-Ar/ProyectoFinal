    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        if(jComboBox2.getItemCount() > 0){
        
            jComboBox2.removeAllItems();
            
        }
        Ciudad bu = (Ciudad)jComboBox1.getSelectedItem();
        PasajeData al= new PasajeData();
        List<Pasaje> pasajes = al.listarPasajes();
        for(Pasaje pasaje : pasajes){
            
            if(pasaje.getCiudadOrigen().getIdCiudad() == bu.getIdCiudad()){
                //getCiudadDestino().getIdCiudad() == bu.getIdCiudad()){
            
                jComboBox2.addItem(pasaje);
                
            }
        
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

