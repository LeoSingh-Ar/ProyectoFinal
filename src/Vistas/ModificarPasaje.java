    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        double importe = Double.parseDouble(jTextField1.getText());
        PasajeData modificar = new PasajeData();
        Pasaje al = new Pasaje();
        Pasaje nuevopasaje = (Pasaje) jComboBox2.getSelectedItem();
        al.setTipoTransporte(jTextField2.getText());
        al.setImporte(importe);
        al.setIdPasaje(nuevopasaje.getIdPasaje());
        al.setEstado(true);
        modificar.modificarPasaje(al);
        
    }//GEN-LAST:event_jButton2ActionPerformed

